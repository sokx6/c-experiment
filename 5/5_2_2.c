/* 采用做标记的办法修改程序，求解约瑟夫问题
 * M个人围成一圈，从第一个人开始依次从1至N循环报数，
 * 每当报数为N时报数人出圈，直到圈中只剩下一个人为止
 */
#include <stdio.h>
#define M 10
#define N 3
int main(void)
{
    int a[M]; /* 数组a存放圈中人的编号，为0表示已出圈 */
    int i, j, k, count, num;

    /* 初始化：对圈中人按顺序编号1—M */
    for (i = 0; i < M; i++)
        a[i] = i + 1;

    count = M; /* 圈中剩余人数 */
    j = -1;    /* 当前报数位置，从-1开始，第一次会移到0 */

    /* 当圈中剩余人数大于1时继续 */
    while (count > 1)
    {
        num = 0; /* 报数计数器 */

        /* 报数，直到报到N */
        while (num < N)
        {
            j = (j + 1) % M; /* 移动到下一个位置，形成环 */
            if (a[j] != 0)   /* 如果该位置的人还在圈中 */
                num++;       /* 报数加1 */
        }

        /* 输出出圈人的编号 */
        printf("%6d", a[j]);

        /* 标记该人出圈 */
        a[j] = 0;
        count--; /* 圈中人数减1 */
    }

    /* 输出最后剩下的人的编号 */
    for (i = 0; i < M; i++)
        if (a[i] != 0)
            printf("%6d\n", a[i]);

    return 0;
}
